class L8P1{ // Integer Literals
	public static void main(String[] ss){
		
		int i = 100; // Decimal Number System Literals
		int j = 0144; // Octal Number System Literals
		int k = 0x64; // Hexa Decimal Number System Literals
		int l = 0b1100100; // Binary Number System Literals
		
		System.out.println("i = "+i);
		System.out.println("j = "+j);
		System.out.println("k = "+k);
		System.out.println("l = "+l);
	}
}