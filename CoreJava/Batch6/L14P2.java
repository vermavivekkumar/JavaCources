class L14P2{ // switch case statement for String expression
		
	public static void main(String[] ss){
		
		String value;
		
		value  = "yellow";
		
		switch(value){ // Here 'value' is a String expression
			case "red":
			System.out.println("red");
			case "green":
			System.out.println("green");
			case "yellow":
			System.out.println("yellow");
			case "black":
			System.out.println("black");
		}
	}
}