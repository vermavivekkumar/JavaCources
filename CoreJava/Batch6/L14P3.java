class L14P3{ // switch case statement for char expression
		
	public static void main(String[] ss){
		
		char value;
		
		value  = 'b';
		
		switch(value){ // Here 'value' is a char expression
			case 'a':
			System.out.println("a");
			case 'd':
			System.out.println("d");
			case 'b':
			System.out.println("b");
			case 'c':
			System.out.println("c");
		}
	}
}